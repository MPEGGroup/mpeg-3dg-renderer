Text::Text() : m_eRectP0( {-1, -1} ), m_eRectP1( {-1, -1} ) { m_eCharacters.clear(); }
Text::~Text() { m_eCharacters.clear(); }

void Text::initialize() {
  Mat4 eProjection( 0.002f, 0.f, 0.f, -1.f, 0.f, 0.002f, 0.f, -1.f, 0.f, 0.f, -1.f, 0.f, 0.f, 0.f, 0.f, 1.f );
  eProjection = glm::transpose( eProjection );
  m_eProgram.create( "Text", g_pTextVertexShader, g_pTextFragmentShader );
  m_eProgram.use();
  m_eProgram.setUniform( "projection", eProjection );
  if ( m_eCharacters.empty() ) { load(); }
  glBindTexture( GL_TEXTURE_2D, 0 );
  glGenVertexArrays( 1, &m_uiVAO );
  glGenBuffers( 1, &m_uiVBO );
  glBindVertexArray( m_uiVAO );
  glBindBuffer( GL_ARRAY_BUFFER, m_uiVBO );
  glBufferData( GL_ARRAY_BUFFER, sizeof( GLfloat ) * 6 * 4, nullptr, GL_DYNAMIC_DRAW );
  glEnableVertexAttribArray( 0 );
  glVertexAttribPointer( 0, 4, GL_FLOAT, GL_FALSE, 4 * sizeof( GLfloat ), nullptr );
  glBindBuffer( GL_ARRAY_BUFFER, 0 );
  glBindVertexArray( 0 );
  m_eProgram.stop();
}

void Text::renderLine( std::string eText, float fX, float fY, float fSize, Vec3 eColor, Vec4 eBack ) {
  Vec4 eColor4 = Vec4( eColor[0], eColor[1], eColor[2], -1.f );
  m_eProgram.use();
  m_eProgram.setUniform( "textColor", eColor4 );
  m_eProgram.setUniform( "backColor", eBack );
  glActiveTexture( GL_TEXTURE0 );
  glBindVertexArray( m_uiVAO );
  if ( m_eRectP0[0] == -1 && m_eRectP0[1] == -1 ) {
    m_eRectP0 = {fX, fY};
    m_eRectP1 = {fX, fY};
  }
  float fScale = fSize / static_cast<float>( g_iCharacterSize );
  for ( std::string::const_iterator c = eText.begin(); c != eText.end(); ++c, fX += 10 * fScale ) {
    Character* pChar = &( m_eCharacters[*c] );
    GLfloat    x     = fX + pChar->getCoordinate()[0] * fScale;
    GLfloat    y     = fY - ( pChar->getSize()[1] - pChar->getCoordinate()[1] ) * fScale;
    GLfloat    w = pChar->getSize()[0] * fScale, h = pChar->getSize()[1] * fScale;
    GLfloat    vertices[6][4] = {{x, y + h, 0, 0}, {x, y, 0, 1},     {x + w, y, 1, 1},
                              {x, y + h, 0, 0}, {x + w, y, 1, 1}, {x + w, y + h, 1, 0}};
    glBindTexture( GL_TEXTURE_2D, pChar->getTexture() );
    glBindBuffer( GL_ARRAY_BUFFER, m_uiVBO );
    glBufferSubData( GL_ARRAY_BUFFER, 0, sizeof( vertices ), vertices );
    glBindBuffer( GL_ARRAY_BUFFER, 0 );
    glDrawArrays( GL_TRIANGLES, 0, 6 );
  }
  m_eRectP1[0] = m_eRectP1[0] > fX ? m_eRectP1[0] : fX;
  m_eRectP1[1] = fY;
  glBindVertexArray( 0 );
  glBindTexture( GL_TEXTURE_2D, 0 );
  m_eProgram.stop();
}

void Text::renderMultiLines( const std::string& eString, GLfloat fSize, Vec3 eColor, Vec4 eBack ) {
  std::string        eLine;
  std::istringstream eStreamOrg( eString );
  float              fX = 2.f * fSize / 5.f, fY = 1000.f - 2.f * fSize;
  initRect();
  while ( std::getline( eStreamOrg, eLine ) ) {
    renderLine( eLine, fX, fY, fSize, eColor, eBack );
    fY -= fSize;
  }
  renderRect( fSize, eBack );
}

void Text::renderRect( float fSize, Vec4 eBack ) {
  m_eProgram.use();
  Vec4 eColor = Vec4( 0.f, 0.f, 0.f, -1.f );
  m_eProgram.setUniform( "textColor", eColor );
  m_eProgram.setUniform( "backColor", eBack );
  glActiveTexture( GL_TEXTURE0 );
  glBindVertexArray( m_uiVAO );
  if ( m_eRectP0[0] > m_eRectP1[0] ) { std::swap( m_eRectP0[0], m_eRectP1[0] ); }
  if ( m_eRectP0[1] > m_eRectP1[1] ) { std::swap( m_eRectP0[1], m_eRectP1[1] ); }
  GLfloat fX0 = m_eRectP0[0] - fSize / 5.f, fY0 = m_eRectP0[1] - 2.0f * fSize / 5.f;
  GLfloat fX1 = m_eRectP1[0] + 1.5f * fSize / 5.f, fY1 = m_eRectP1[1] + 6.0f * fSize / 5.f;
  GLfloat ppVertices[6][4] = {{fX0, fY1, -1, -1}, {fX0, fY0, -1, -1}, {fX1, fY0, -1, -1},
                              {fX0, fY1, -1, -1}, {fX1, fY0, -1, -1}, {fX1, fY1, -1, -1}};
  glBindTexture( GL_TEXTURE_2D, 0 );
  glBindBuffer( GL_ARRAY_BUFFER, m_uiVBO );
  glBufferSubData( GL_ARRAY_BUFFER, 0, sizeof( ppVertices ), ppVertices );
  glBindBuffer( GL_ARRAY_BUFFER, 0 );
  glDrawArrays( GL_TRIANGLES, 0, 6 );
  glBindVertexArray( 0 );
  glBindTexture( GL_TEXTURE_2D, 0 );
  m_eProgram.stop();
}

void Text::load() {
  int iIndex = 0;
  glPixelStorei( GL_UNPACK_ALIGNMENT, 1 );
  for ( int i = 0; i < g_iCharacterNumber; i++ ) {
    GLuint uiTexture;
    glGenTextures( 1, &uiTexture );
    glBindTexture( GL_TEXTURE_2D, uiTexture );
    glTexImage2D( GL_TEXTURE_2D, 0, GL_RED, g_pWidth[i], g_pHeight[i], 0, GL_RED, GL_UNSIGNED_BYTE,
                  g_pCharacters + iIndex );
    glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE );
    glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE );
    glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR );
    glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR );
    Character eCharacter( uiTexture, Vec2( static_cast<float>( g_pWidth[i] ), static_cast<float>( g_pHeight[i] ) ),
                          Vec2( static_cast<float>( g_pLeft[i] ), static_cast<float>( g_pTop[i] ) ) );
    iIndex += g_pWidth[i] * g_pHeight[i];
    m_eCharacters.push_back( eCharacter );
  }
}
